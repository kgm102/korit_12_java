package Loop01;
// 더블 와일과 비교한것
public class Test2 {
    public static void main(String[] args) {

        /*
            좀전에 한거 for문으로 한 번 해보기
         */

        for(int i = 0; i < 5; i++){
            for(int j = 0; j < 3; j++) {
                System.out.println((i + 1) + "일차" + (j+1) + "교시입니다.");
            }
        }

    }
}
